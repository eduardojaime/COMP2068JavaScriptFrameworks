// this is a regular function in javascript (open)
// declare a function that increases a number by 1
function counter() {
    // declare variable
    let count = 0;
    // increase the count by 1
    count++;
    // print the count
    console.log(count); 
} // associated variables are destroyed when execution is done

// call function twice, what's the output?
counter(); // 1
counter(); // 1